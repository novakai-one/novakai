/* =====================================================================
   inspector.ts — the right-hand property editor
   ---------------------------------------------------------------------
   Responsibility: render the Inspector pane for whatever is selected —
   a single node (label/shape/fill/size/pos), multiple nodes (align,
   distribute, bulk fill, group, delete), or an edge (label/style/routing/
   reverse/delete). Wires each control back to the model + render/sync/
   history. Read-render-write against the current selection.

   Depends on nodes (align/group/delete) and selection (clearSel).
   ===================================================================== */

import type { AppContext } from '../../core/context/context';
import type { NodesApi } from '../../interaction/nodes';
import type { SelectionApi } from '../../interaction/selection';
import type { NodeKind, DiagramNode, DiagramEdge } from '../../core/types/types';
import type { StateStore } from '../../core/state/state';
import { SHAPES, KINDS, PALETTE, PALETTE_NAMES, STYLES, DEFAULTS, esc } from '../../core/config/config';
import { isAncestor } from '../../core/state/state';
import { initInspectorFrontmatter } from './inspector-frontmatter';

export interface InspectorApi {
  renderInspector: () => void;
}

/** Bag of "stable across a single-inspector session" deps, kept to one param each call. */
interface SingleInspectorArgs {
  ctx: AppContext;
  nodesApi: NodesApi;
  renderInspector: () => void;
  fmEditor: ReturnType<typeof initInspectorFrontmatter>;
}

/** shape of one bodies.json entry (ctx.bodies value type, named for readability). */
type SourceEntry = { kind: string; body: string; accepts?: string[]; returns?: string | null };

function byId(id: string): HTMLElement {
  return document.getElementById(id) as HTMLElement;
}

function setInspectorVisibility(inspEmpty: HTMLElement, inspBody: HTMLElement, hasSelection: boolean): void {
  inspEmpty.style.display = hasSelection ? 'none' : 'block';
  inspBody.style.display = hasSelection ? 'flex' : 'none';
}

/* ---------- status line ---------- */

function renderStatusPane(state: StateStore): void {
  const statusEl = document.getElementById('status');
  if (!statusEl) return;
  const nodeCount = Object.keys(state.nodes).length;
  const edgeCount = state.edges.length;
  let statusText = `${nodeCount} node${nodeCount !== 1 ? 's' : ''}`;
  statusText += ` · ${edgeCount} edge${edgeCount !== 1 ? 's' : ''}`;
  if (state.sel.size) statusText += ` · ${state.sel.size} selected`;
  statusEl.textContent = statusText;
}

/* ---------- source pane ---------- */

function hideSourceBody(sourceEmpty: HTMLElement, sourceBody: HTMLElement): void {
  sourceEmpty.style.display = 'block';
  sourceBody.style.display = 'none';
}

function noBodiesMessage(): string {
  return 'Select a node to view its source code.<br><br>Load a <code>bodies.json</code> with the '
    + '<b>Bodies</b> button (top-right) — generated by <code>extract.mjs --bodies</code> against '
    + 'your TS project. Read locally; nothing is uploaded.';
}

function noSourceMessage(id: string): string {
  return `No source found for <code>${esc(id)}</code>.<br>Ensure the node id has a `
    + `<code>%% src</code> directive in the loaded bodies.json.`;
}

function showMissingSource(sourceEmpty: HTMLElement, sourceBody: HTMLElement, ctx: AppContext, id: string): void {
  hideSourceBody(sourceEmpty, sourceBody);
  sourceEmpty.innerHTML = ctx.bodies ? noSourceMessage(id) : noBodiesMessage();
}

function buildSourceSignatureHtml(entry: SourceEntry): string {
  const accepts = entry.accepts ?? [];
  const returns = entry.returns ?? null;
  if (!accepts.length && !returns) return '';
  const params = accepts.map((arg) => `  ${esc(arg)}`).join(',\n');
  const head = accepts.length ? `(\n${params}\n)` : '()';
  const tail = returns ? ` → ${esc(returns)}` : '';
  return `<span class="src-sig">${head}${tail}</span>`;
}

function buildSourceBodyHtml(entry: SourceEntry): string {
  const sig = buildSourceSignatureHtml(entry);
  return `<span class="src-kind">${esc(entry.kind)}</span>${sig}${esc(entry.body)}`;
}

function renderSourcePane(ctx: AppContext): void {
  const sourceEmpty = document.getElementById('sourceEmpty');
  const sourceBody = document.getElementById('sourceBody');
  if (!sourceEmpty || !sourceBody) return;
  const { state } = ctx;
  if (state.sel.size !== 1) return hideSourceBody(sourceEmpty, sourceBody);
  const id = [...state.sel][0];
  const entry = ctx.bodies?.get(id);
  if (!entry) return showMissingSource(sourceEmpty, sourceBody, ctx, id);
  sourceEmpty.style.display = 'none';
  sourceBody.style.display = 'block';
  sourceBody.innerHTML = buildSourceBodyHtml(entry);
}

/* ---------- single-node inspector: markup ---------- */

function shapeOptionsHtml(node: DiagramNode): string {
  return SHAPES.map((shape) =>
    `<option value="${shape}" ${shape === node.shape ? 'selected' : ''}>${shape}</option>`).join('');
}

function kindOptionsHtml(node: DiagramNode): string {
  const none = `<option value="" ${!node.kind ? 'selected' : ''}>(none)</option>`;
  const rest = KINDS.map((kind) =>
    `<option value="${kind}" ${kind === node.kind ? 'selected' : ''}>${kind}</option>`).join('');
  return none + rest;
}

function parentOptionHtml(state: StateStore, node: DiagramNode, pid: string): string {
  const parentNode = state.nodes[pid];
  const groupSuffix = parentNode.shape === 'group' ? ' (group)' : '';
  const selected = pid === node.parent ? 'selected' : '';
  return `<option value="${pid}" ${selected}>${esc(parentNode.label)}${groupSuffix}</option>`;
}

function parentOptionsHtml(state: StateStore, node: DiagramNode): string {
  const top = `<option value="" ${!node.parent ? 'selected' : ''}>Top level</option>`;
  const rest = Object.keys(state.nodes)
    .filter((pid) => pid !== node.id && !isAncestor(state, node.id, pid))
    .map((pid) => parentOptionHtml(state, node, pid))
    .join('');
  return top + rest;
}

function swatchesHtml(selectedColor: string | null): string {
  return PALETTE.map((color, i) =>
    `<div class="sw ${color === selectedColor ? 'on' : ''}" data-c="${color}" `
    + `title="${PALETTE_NAMES[i]}" style="background:${color}"></div>`).join('');
}

function sizePosFieldsHtml(node: DiagramNode): string {
  return `
    <div class="insp-sec-title">Size & position</div>
    <div class="row2">
      <div class="field"><label>W</label><input id="iW" type="number" value="${Math.round(node.w)}"></div>
      <div class="field"><label>H</label><input id="iH" type="number" value="${Math.round(node.h)}"></div>
    </div>
    <div class="row2">
      <div class="field"><label>X</label><input id="iX" type="number" value="${Math.round(node.x)}"></div>
      <div class="field"><label>Y</label><input id="iY" type="number" value="${Math.round(node.y)}"></div>
    </div>`;
}

function buildSingleInspectorHtml(state: StateStore, node: DiagramNode): string {
  return `
    <div class="field"><label>Label</label><input id="iLabel" value="${esc(node.label)}"></div>
    <div class="field"><label>Shape</label><select id="iShape">${shapeOptionsHtml(node)}</select></div>
    <div class="field"><label>Kind</label><select id="iKind">${kindOptionsHtml(node)}</select></div>
    <div class="field"><label>Parent (drill container)</label>
      <select id="iParent">${parentOptionsHtml(state, node)}</select></div>
    <div class="field"><label>Fill</label>
      <div class="swatches" id="iSw">${swatchesHtml(node.color)}</div></div>
    ${sizePosFieldsHtml(node)}
    <div id="fmHost" class="fm-host"></div>
    <button class="filebtn danger" id="iDel">Delete node</button>`;
}

/* ---------- single-node inspector: wiring ---------- */

function wireLabelField(ctx: AppContext, node: DiagramNode): void {
  const labelInput = byId('iLabel') as HTMLInputElement;
  labelInput.oninput = (e) => {
    node.label = (e.target as HTMLInputElement).value;
    ctx.hooks.render();
    ctx.hooks.sync();
  };
  labelInput.onchange = () => ctx.hooks.pushHistory();
}

function wireShapeField(ctx: AppContext, node: DiagramNode, renderInspector: () => void): void {
  const shapeSelect = byId('iShape') as HTMLSelectElement;
  shapeSelect.onchange = (e) => {
    node.shape = (e.target as HTMLSelectElement).value as typeof node.shape;
    void DEFAULTS[node.shape];
    ctx.hooks.render();
    ctx.hooks.sync();
    renderInspector();
    ctx.hooks.pushHistory();
  };
}

function wireKindField(nodesApi: NodesApi, node: DiagramNode): void {
  const kindSelect = byId('iKind') as HTMLSelectElement;
  kindSelect.onchange = (e) => {
    const value = (e.target as HTMLSelectElement).value;
    nodesApi.setNodeMeta(node.id, { kind: value ? (value as NodeKind) : null });
  };
}

function wireParentField(ctx: AppContext, node: DiagramNode, renderInspector: () => void): void {
  const parentSelect = byId('iParent') as HTMLSelectElement;
  parentSelect.onchange = (e) => {
    const value = (e.target as HTMLSelectElement).value;
    node.parent = value || null;
    ctx.hooks.render();
    ctx.hooks.sync();
    renderInspector();
    ctx.hooks.pushHistory();
  };
}

function wireSwatches(ctx: AppContext, node: DiagramNode, renderInspector: () => void): void {
  byId('iSw').querySelectorAll('.sw').forEach((swatchRaw) => {
    const swatch = swatchRaw as HTMLElement;
    swatch.onclick = () => {
      node.color = swatch.dataset.c === 'null' ? null : (swatch.dataset.c as string);
      ctx.hooks.render();
      ctx.hooks.sync();
      renderInspector();
      ctx.hooks.pushHistory();
    };
  });
}

function applySizePosField(node: DiagramNode, field: 'W' | 'H' | 'X' | 'Y', value: number): void {
  if (field === 'W') node['w'] = Math.max(40, value);
  if (field === 'H') node['h'] = Math.max(30, value);
  if (field === 'X') node.x = value;
  if (field === 'Y') node.y = value;
}

function wireSizePosFields(ctx: AppContext, node: DiagramNode): void {
  (['W', 'H', 'X', 'Y'] as const).forEach((field) => {
    const input = byId('i' + field) as HTMLInputElement;
    input.oninput = () => {
      applySizePosField(node, field, +input.value || 0);
      ctx.hooks.render();
      ctx.hooks.sync();
    };
    input.onchange = () => ctx.hooks.pushHistory();
  });
}

function wireDeleteButton(nodesApi: NodesApi): void {
  (byId('iDel') as HTMLButtonElement).onclick = () => nodesApi.deleteSelection();
}

function wireSingleInspector(args: SingleInspectorArgs, node: DiagramNode): void {
  wireLabelField(args.ctx, node);
  wireShapeField(args.ctx, node, args.renderInspector);
  wireKindField(args.nodesApi, node);
  wireParentField(args.ctx, node, args.renderInspector);
  wireSwatches(args.ctx, node, args.renderInspector);
  wireSizePosFields(args.ctx, node);
  wireDeleteButton(args.nodesApi);
  args.fmEditor.renderFrontmatterSection(byId('fmHost'), node);
}

function runSingleInspector(args: SingleInspectorArgs, body: HTMLElement): void {
  const node = args.ctx.state.nodes[[...args.ctx.state.sel][0]];
  body.innerHTML = buildSingleInspectorHtml(args.ctx.state, node);
  wireSingleInspector(args, node);
}

/* ---------- multi-node inspector ---------- */

function multiSwatchesHtml(): string {
  return PALETTE.map((color, i) =>
    `<div class="sw" data-c="${color}" title="${PALETTE_NAMES[i]}" style="background:${color}"></div>`).join('');
}

