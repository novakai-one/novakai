function wireEdgeResetButton(ctx: AppContext, edge: DiagramEdge): void {
  (byId('eReset') as HTMLButtonElement).onclick = () => {
    edge.bend = null;
    edge.labelPos = null;
    ctx.hooks.render();
    ctx.hooks.sync();
    ctx.hooks.reroute();
    ctx.hooks.pushHistory();
  };
}

function wireEdgeFlipButton(nodesApi: NodesApi, edge: DiagramEdge): void {
  (byId('eFlip') as HTMLButtonElement).onclick = () => nodesApi.reverseEdge(edge.id);
}

function wireEdgeDeleteButton(nodesApi: NodesApi, edge: DiagramEdge): void {
  (byId('eDel') as HTMLButtonElement).onclick = () => nodesApi.deleteEdge(edge.id);
}

function wireEdgeInspector(ctx: AppContext, nodesApi: NodesApi, edge: DiagramEdge): void {
  wireEdgeLabelField(ctx, nodesApi, edge);
  wireEdgeStyleField(ctx, edge);
  wireEdgeRouteField(ctx, edge);
  wireEdgeResetButton(ctx, edge);
  wireEdgeFlipButton(nodesApi, edge);
  wireEdgeDeleteButton(nodesApi, edge);
}

function runEdgeInspector(ctx: AppContext, nodesApi: NodesApi, selectionApi: SelectionApi, body: HTMLElement): void {
  const edge = ctx.state.edges.find((candidate) => candidate.id === ctx.state.selEdge);
  if (!edge) return selectionApi.clearSel();
  body.innerHTML = buildEdgeInspectorHtml(edge, ctx.state.nodes[edge.from], ctx.state.nodes[edge.to]);
  wireEdgeInspector(ctx, nodesApi, edge);
}

type InspectorRenderers = {
  edge: (body: HTMLElement) => void; multi: (body: HTMLElement) => void; single: (body: HTMLElement) => void;
};

/** which body-render call to make, keyed by selection shape — kept as one module-scope
    call so renderInspector's own dispatch collapses to a single statement. */
function dispatchInspectorRender(ctx: AppContext, renderers: InspectorRenderers, body: HTMLElement): void {
  if (ctx.state.selEdge) renderers.edge(body);
  else if (ctx.state.sel.size > 1) renderers.multi(body);
  else if (ctx.state.sel.size > 0) renderers.single(body);
}

/* ---------- init ---------- */

export function initInspector(ctx: AppContext, nodes: NodesApi, selection: SelectionApi): InspectorApi {
  const inspEmpty = byId('inspEmpty'), inspBody = byId('inspBody'), fmEditor = initInspectorFrontmatter(ctx);

  function renderInspector(): void {
    setInspectorVisibility(inspEmpty, inspBody, ctx.state.sel.size > 0 || !!ctx.state.selEdge);
    dispatchInspectorRender(
      ctx, { edge: renderEdgeInspector, multi: renderMultiInspector, single: renderSingleInspector }, inspBody);
    updateStatus();
    updateSource(); }

  function updateSource(): void {
    renderSourcePane(ctx); }

  function updateStatus(): void {
    renderStatusPane(ctx.state); }

  function renderSingleInspector(body: HTMLElement): void {
    runSingleInspector({ ctx, nodesApi: nodes, renderInspector, fmEditor }, body); }

  function renderMultiInspector(body: HTMLElement): void {
    runMultiInspector(ctx, nodes, body); }
  function renderEdgeInspector(body: HTMLElement): void {
    runEdgeInspector(ctx, nodes, selection, body); }

  return { renderInspector };
}
